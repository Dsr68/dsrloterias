var numeros = [];
var frequencia = 0;
var valor = 0;

function contar(n_sorteados){
	for(i = 0; i < n_sorteados.length; i++){
			for(j = 0; j < n_sorteados.length; j++){
				if(n_sorteados[i] == n_sorteados[j]){
					++frequencia;				
				}			
			}
			numeros[i] = {frequencia: frequencia, valor: valor};
	}
}

function ordenar(){
	var ordenado = numeros.sort();
	
	return ordenado;
}

